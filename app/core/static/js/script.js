// Función para abrir el menú lateral
function abrirMenu() {
    document.getElementById("menu-lateral").style.left = "0";  // Desplazamos el menú hacia la derecha
}

// Función para cerrar el menú lateral
function cerrarMenu() {
    document.getElementById("menu-lateral").style.left = "-250px";  // Ocultamos el menú
}
